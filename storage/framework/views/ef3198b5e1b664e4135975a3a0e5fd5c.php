<script>
    document.getElementById('save-theme-settings').addEventListener('click', async function() {
        try {
            const settings = {
                theme: document.querySelector('input[name="data-bs-theme"]:checked')?.value || 'light',
                layoutStyle: document.querySelector('input[name="data-layout-style"]:checked')?.value || 'default',
                sidebar: document.querySelector('input[name="data-sidebar"]:checked')?.value || 'light',
                sidebarSize: document.querySelector('input[name="data-sidebar-size"]:checked')?.value || 'lg',
                sidebarImage: document.querySelector('input[name="data-sidebar-image"]:checked')?.value || 'none'
            };

            const response = await fetch('/update-theme', {
                method: 'POST',
                headers: {
                    'Content-Type': 'application/json',
                    'X-CSRF-TOKEN': document.querySelector('meta[name="csrf-token"]').content
                },
                body: JSON.stringify(settings)
            });

            if (response.ok) {
                // Show success message
                Swal.fire({
                    title: 'Success!',
                    text: 'Theme settings saved successfully',
                    icon: 'success',
                    showConfirmButton: false,
                    timer: 1500
                });

                // Store in localStorage
                Object.entries(settings).forEach(([key, value]) => {
                    localStorage.setItem(key, value);
                });
            } else {
                throw new Error('Failed to save settings');
            }
        } catch (error) {
            console.error('Error saving theme settings:', error);
            Swal.fire({
                title: 'Error!',
                text: 'Failed to save theme settings',
                icon: 'error',
                confirmButtonText: 'Ok'
            });
        }
    });
</script>
